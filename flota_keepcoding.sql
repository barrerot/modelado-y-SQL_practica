-- flota_keepcoding.coche definition


CREATE TABLE flota_keepcoding.coche (
	id_coche serial4 NOT NULL,
	modelo varchar(25) NOT NULL,
	id_marca int4 NULL,
	color varchar(15) NULL,
	matricula varchar(15) NULL,
	kilometros int4 NULL,
	id_aseguradora int4 NULL,
	numero_poliza varchar(25) NULL,
	fecha_compra date NOT NULL,
	fecha_alta_seguro date NOT NULL,
