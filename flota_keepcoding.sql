-- Crear la tabla Grupo
CREATE table flota_keepcoding.grupo (
	id_grupo serial4 NOT NULL,
	nombre_grupo varchar(50) NOT NULL,
	CONSTRAINT grupo_pkey PRIMARY KEY (id_grupo)
