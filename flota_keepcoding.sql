);


-- flota_keepcoding.revision definition


CREATE TABLE flota_keepcoding.revision (
	id_revision serial4 NOT NULL,
	id_coche int4 NULL,
	kilometros_revision int4 NULL,
	fecha_revision date NULL,
	importe float8 NULL,
